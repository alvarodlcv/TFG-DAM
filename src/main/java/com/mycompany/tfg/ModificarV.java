    private void servicioboxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_servicioboxActionPerformed
if (vehiculo != null) {
        // Obtener el nuevo servicio seleccionado del JComboBox como un String
        String nuevoServicio = (String) serviciobox.getSelectedItem();
        
        // Verificar si se seleccionó una opción válida
        if (nuevoServicio != null) {
            // Actualizar el servicio del vehículo en la base de datos
            vehiculoDAO.actualizarCampoVehiculoServicio(vehiculo, nuevoServicio);
            
            // Mostrar un mensaje de éxito
            JOptionPane.showMessageDialog(null, "Servicio del vehículo actualizado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Mostrar un mensaje de error si no se seleccionó ninguna opción válida
            JOptionPane.showMessageDialog(null, "Debe seleccionar una opción válida.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        // Mostrar un mensaje de error si no hay vehículo seleccionado
        JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún vehículo.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_servicioboxActionPerformed


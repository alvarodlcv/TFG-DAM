    private void buscarbut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbut1ActionPerformed
        // TODO add your handling code here:
         String matricula = matriculatx.getText();
         String propietario = proptx.getText();
         
    // Llamar al método buscarVehiculo del VehiculoDAO
    VehiculoDAO vehiculoDAO = new VehiculoDAO();
    List<Vehiculo> vehiculosEncontrados = vehiculoDAO.buscarVehiculo(matricula, propietario);

    // Mostrar el primer vehículo encontrado en el TextField txtResultado
    if (!vehiculosEncontrados.isEmpty()) {
        vehiculo = vehiculosEncontrados.get(0);
        System.out.println(vehiculo);
    
       String infoVehiculo = "Marca: " + vehiculo.getMarcaV() + "\n"  +
                      "Modelo: " + vehiculo.getModeloV() + "\n"  +
                      "Año: " + vehiculo.getAñoV() + "\n" +
                      "Kilometraje: " + vehiculo.getKilometrajeV() + " km"+ "\n" +
                      "Fecha caducidad ITV: " + vehiculo.getItvV() + "\n"  +
                      "Color: " + vehiculo.getColorV() + "\n" +
                      "Matrícula: " + vehiculo.getMatriculaV() + "\n"+
                      "Propietario: " + vehiculo.getPropietarioV() + "\n"+
                      "Servicio: " + vehiculo.getServicioV();

                // Ajustar el tamaño del TextField
                FontMetrics metrics = textofieldoM.getFontMetrics(textofieldoM.getFont());
                int width = metrics.stringWidth(infoVehiculo);
                int height = metrics.getHeight();
                textofieldoM.setPreferredSize(new Dimension(width, height));

        textofieldoM.setText(infoVehiculo);
    } else {
        textofieldoM.setText("Vehículo no encontrado.");
        System.out.println("Vehiculo empty");
    }

    // Limpiar los campos después de la búsqueda (opcional)
    matriculatx.setText("");
    proptx.setText("");

    }//GEN-LAST:event_buscarbut1ActionPerformed
    

    private void telefonobutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_telefonobutActionPerformed
        // TODO add your handling code here:
        if (cliente != null) {
            // Obtener el valor actualizado del campo de texto mtfield
            String nuevotelefono = mttfield.getText();
            int nuevoValor = Integer.parseInt(nuevotelefono);

            // Actualizar solo el campo de marca en la base de datos
            clientesDAO.actualizarCampoClienteTelefono(cliente,nuevoValor);

            // Mostrar un mensaje de éxito
            JOptionPane.showMessageDialog(null, "Teléfono del cliente modificado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Mostrar un mensaje de error si no hay vehículo seleccionado
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún cliente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        mttfield.setText("");

    }//GEN-LAST:event_telefonobutActionPerformed

    private void direccionbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_direccionbutActionPerformed
        // TODO add your handling code here:
        if (cliente != null) {
            // Obtener el valor actualizado del campo de texto mtfield
            String nuevadir = mttfield.getText();
            String tipoDir = combodir.getSelectedItem().toString();
            String nuevardire = (tipoDir + " " + nuevadir);
            // Actualizar solo el campo de marca en la base de datos
            clientesDAO.actualizarCampoClienteDir(cliente,nuevardire);

            // Mostrar un mensaje de éxito
            JOptionPane.showMessageDialog(null, "Dirección del cliente modificado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Mostrar un mensaje de error si no hay vehículo seleccionado
            JOptionPane.showMessageDialog(null, "No se ha seleccionado ningún cliente.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        mttfield.setText("");
    }//GEN-LAST:event_direccionbutActionPerformed


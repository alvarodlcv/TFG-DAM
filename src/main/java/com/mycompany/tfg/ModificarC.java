    private void buscarbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbutActionPerformed
        // TODO add your handling code here:
        String dni = dnitextMod.getText();
        ClientesDAO clientesDAO = new ClientesDAO();
        List<Cliente> clientesEncontrados = clientesDAO.buscarCliente(dni);

        if (!clientesEncontrados.isEmpty()) {
           cliente = clientesEncontrados.get(0);
            System.out.println(cliente);

            String infoCliente = "Nombre: " + cliente.getNombreC()+ "\n"  +
            "Primer apellido: " + cliente.getApellido1C()+ "\n"  +
            "Segundo apellido: " + cliente.getApellido2C()+ "\n" +
            "Teléfono: " + cliente.getTelefonoC()+ "\n" +
            "DNI / NIF: " + cliente.getDniC()+ "\n"  +
            "Dirección: " + cliente.getDireccionC()+ "\n" +
            "Correo electrónico: " + cliente.getCorreoC()+ "\n"+
            "Coches en propiedad: " + cliente.getCochesPropC()+ "\n"+
            "¿Cómo nos conociste?: " + cliente.getConocisteC();

            // Ajustar el tamaño del TextField
            FontMetrics metrics = textoAreo.getFontMetrics(textoAreo.getFont());
            int width = metrics.stringWidth(infoCliente);
            int height = metrics.getHeight();
            textoAreo.setPreferredSize(new Dimension(width, height));

            textoAreo.setText(infoCliente);
        } else {
            textoAreo.setText("Cliente no encontrado.");
        }
    }//GEN-LAST:event_buscarbutActionPerformed


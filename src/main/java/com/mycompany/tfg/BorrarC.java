    private void buscarbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbutActionPerformed
        // TODO add your handling code here:
        String dni = dnitxbuscar.getText();
        ClientesDAO clientesDAO = new ClientesDAO();
        List<Cliente> clientesEncontrados = clientesDAO.buscarCliente(dni);

        if (!clientesEncontrados.isEmpty()) {
        Cliente cEncontrado = clientesEncontrados.get(0);

    
       String infoCliente = "Nombre: " + cEncontrado.getNombreC()+ "\n"  +
                      "Primer apellido: " + cEncontrado.getApellido1C()+ "\n"  +
                      "Segundo apellido: " + cEncontrado.getApellido2C()+ "\n" +
                      "Tel√©fono: " + cEncontrado.getTelefonoC()+ "\n" +
                      "DNI / NIF: " + cEncontrado.getDniC()+ "\n"  +
                      "Direcci√≥n: " + cEncontrado.getDireccionC()+ "\n" +
                      "Correo electr√≥nico: " + cEncontrado.getCorreoC()+ "\n"+
                      "Coches en propiedad: " + cEncontrado.getCochesPropC()+ "\n"+
                      "¬øC√≥mo nos conociste?: " + cEncontrado.getConocisteC();

                // Ajustar el tama√±o del TextField
               FontMetrics metrics = textoAreo.getFontMetrics(textoAreo.getFont());
                int width = metrics.stringWidth(infoCliente);
                int height = metrics.getHeight();
                textoAreo.setPreferredSize(new Dimension(width, height));

        textoAreo.setText(infoCliente);
    } else {
        textoAreo.setText("Cliente no encontrado.");
    }
    }//GEN-LAST:event_buscarbutActionPerformed

    private void borrarbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarbutActionPerformed
        // TODO add your handling code here:
          // TODO add your handling code here:
        String dni = dnitxbuscar.getText(); 
        
        if (dni.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el DNI / NIF para borrar el cliente.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(null, "¬øEst√°s seguro de que deseas borrar este cliente?", "Confirmar Borrado", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            clientesDAO.borrarCliente(dni);
            JOptionPane.showMessageDialog(null, "Veh√≠culo borrado exitosamente.", "Borrado Exitoso", JOptionPane.INFORMATION_MESSAGE);
            // Puedes limpiar los campos o actualizar la interfaz aqu√≠ si es necesario
        }
        textoAreo.setText("");
        dnitxbuscar.setText("");
    }//GEN-LAST:event_borrarbutActionPerformed


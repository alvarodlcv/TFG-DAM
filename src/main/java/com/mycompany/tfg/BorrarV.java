    private void buscarbut1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarbut1ActionPerformed
        // TODO add your handling code here:
    String matricula = borrarmatriculatx.getText();
    String propietario = borrarproptx.getText();

    // Llamar al método buscarVehiculo del VehiculoDAO
    VehiculoDAO vehiculoDAO = new VehiculoDAO();
    List<Vehiculo> vehiculosEncontrados = vehiculoDAO.buscarVehiculo(matricula, propietario);

    // Mostrar el primer vehículo encontrado en el TextField txtResultado
    if (!vehiculosEncontrados.isEmpty()) {
        Vehiculo vehiculoEncontrado = vehiculosEncontrados.get(0);

   
    
       String infoVehiculo = "Marca: " + vehiculoEncontrado.getMarcaV() + "\n"  +
                      "Modelo: " + vehiculoEncontrado.getModeloV() + "\n"  +
                      "Año: " + vehiculoEncontrado.getAñoV() + "\n" +
                      "Kilometraje: " + vehiculoEncontrado.getKilometrajeV() + " km"+ "\n" +
                      "Fecha caducidad ITV: " + vehiculoEncontrado.getItvV() + "\n"  +
                      "Color: " + vehiculoEncontrado.getColorV() + "\n" +
                      "Matrícula: " + vehiculoEncontrado.getMatriculaV() + "\n"+
                      "Propietario: " + vehiculoEncontrado.getPropietarioV() + "\n"+
                      "Servicio: " + vehiculoEncontrado.getServicioV();

                // Ajustar el tamaño del TextField
               FontMetrics metrics = textofieldoB.getFontMetrics(textofieldoB.getFont());
                int width = metrics.stringWidth(infoVehiculo);
                int height = metrics.getHeight();
                textofieldoB.setPreferredSize(new Dimension(width, height));

        textofieldoB.setText(infoVehiculo);
    } else {
        textofieldoB.setText("Vehículo no encontrado.");
    }

    // Limpiar los campos después de la búsqueda (opcional)
    
    }//GEN-LAST:event_buscarbut1ActionPerformed


    private void borrarbutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarbutActionPerformed
        // TODO add your handling code here:
        String matricula = borrarmatriculatx.getText(); // Obtener la matrícula del JTextField
        String propietario = borrarproptx.getText(); // Obtener el propietario del JTextField

        if (matricula.isEmpty() || propietario.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese la matrícula y el propietario para borrar el vehículo.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Estás seguro de que deseas borrar este vehículo?", "Confirmar Borrado", JOptionPane.YES_NO_OPTION);
        if (confirmacion == JOptionPane.YES_OPTION) {
            vehiculoDAO.borrarVehiculo(matricula, propietario);
            JOptionPane.showMessageDialog(null, "Vehículo borrado exitosamente.", "Borrado Exitoso", JOptionPane.INFORMATION_MESSAGE);
            // Puedes limpiar los campos o actualizar la interfaz aquí si es necesario
        }
        textofieldoB.setText("");
    }//GEN-LAST:event_borrarbutActionPerformed

